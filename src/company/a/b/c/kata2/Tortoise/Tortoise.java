package company.a.b.c.kata2.Tortoise;

public class Tortoise {

    public static String replace(final String s) {
        return s.replace("i", "!")
                .replace("I", "!")
                .replace("A", "!")
                .replace("a", "!")
                .replace("e", "!")
                .replace("E", "!")
                .replace("o", "!")
                .replace("O", "!")
                .replace("u", "!")
                .replace("U", "!");
    }
}